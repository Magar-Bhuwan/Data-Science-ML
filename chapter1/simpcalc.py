a = float(input("Enter first number: "))
b = float(input("Enter second number: "))
print("Choose operator: +, -, *, /")
operator = input("Enter operator: ")

if operator == '+':
    print("Addition = ", a + b)
    
elif operator == '-':
    print("Subtraction = ", a - b)

elif operator == '*':
    print("Multiply = ", a * b)

elif operator == '/':
    print("Division  = ", a/b)

print ("Thank you! ")



